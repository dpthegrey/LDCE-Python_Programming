
# importing the required module
import matplotlib.pyplot as plt

# x axis values
x = [1, 2, 3, 4]
# corresponding y axis values
y = [1, 4, 9, 16]

# plotting the points
plt.plot(x, y)

# naming the x axis
plt.xlabel('x - axis')
# naming the y axis
plt.ylabel('y - axis')

# giving a title to my graph
plt.title('Q.4 (b)')

# function to show the plot
plt.show()
